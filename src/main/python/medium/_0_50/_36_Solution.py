# 36. Valid Sudoku
from collections import deque
from typing import List


class _36_Solution:
    def isValidSudoku(self, board: List[List[str]]) -> bool:
        visited = set()
        for i in range(9):
            for j in range(9):
                if board[i][j] != '.':
                    partial = "(" + board[i][j] + ")"
                    if (str(i) + partial in visited): return False
                    if (partial + str(j) in visited): return False
                    if (str(i // 3) + partial + str(j // 3) in visited): return False
                    visited.add(str(i) + partial)
                    visited.add(partial + str(j))
                    visited.add(str(i // 3) + partial + str(j // 3))
        return True




