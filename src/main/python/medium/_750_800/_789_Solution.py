# 789. Escape The Ghosts
from typing import List


class _789_Solution:
    def escapeGhosts(self, ghosts: List[List[int]], target: List[int]) -> bool:
        maximal = abs(target[0]) + abs(target[1])
        for lst in ghosts:
            dis = abs(lst[0] - target[0]) + abs(lst[1] - target[1])
            if dis <= maximal: return False
        return True


